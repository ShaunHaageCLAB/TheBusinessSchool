    <p>
        <strong>PROGRAMME EVALUATION</strong>
    </p>
    <p>The SLP assessment involves a test and a formal examination at the close of the programme.</p>

    <p>
        <strong>PROGRAMME OUTLINE</strong>
    </p>

    <ul>
        <li>The use and utilisation of the system navigator.</li>
        <li>How to set-up and create a new company within Sage One accounting.</li>
        <li>Creating and editing the accounts and codes for a company and taking on an opening balance from other systems.</li>
        <li>Processing orders and purchases for both suppliers and creditors.</li>
        <li>How to use Sage One for sales in relation to customers and debtors.</li>
        <li>Utilising the Sage One cash book for payments/deposits and petty cash.</li>
        <li>Compiling bank reconciliation and month-end reports.</li>
    </ul>
</div>

