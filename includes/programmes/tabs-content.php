<div data-panel="panel2" class="panel__content">
    <p>
        <strong>WHO SHOULD ATTEND</strong>
    </p>
    <ul>Students should meet the following requirements:
        <li>Those who wish to keep a computerised set of accounts for a small to medium-sized business.</li>
        <li>Bookkeepers who wish to gain the practical knowledge of electronic bookkeeping.</li>
    </ul>

    <p>
        <strong>DURATION</strong>
        <br> Lectures are conducted twice a week in the evening or on a Saturday morning over 7 weeks.
    </p>
</div>

<div data-panel="panel3" class="panel__content">
