    <p>
        <strong>INTRODUCTION</strong>
    </p>
    <p>
        The days of manually recording financial information are a thing of the past. Today's modern bookkeeper makes full use of
        the calculating power of computerised accounting. Using the Sage One accounting system, bookkeepers are not only
        improving their efficiency but are also saving valuable time. This short learning programme (SLP) offers those
        with a basic understanding of bookkeeping and the Windows® environment an opportunity to extend their expertise
        and administer an electronic set of accounts.
    </p>
